                <p>Está formada por voluntarios que se constituyen como asociación en 1989 con la finalidad de servir de apoyo a la comunidad ofreciendo servicios dirigidos a las personas con discapacidad y sus familias.
                Realiza un seguimiento exhaustivo y periódico a través de reuniones y contacto directo con la persona responsable de la gestión y dirección del centro. </p>
