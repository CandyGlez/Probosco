                <p class="text-justify" >Se trata de una convocatoria abierta a entidades que trabajan con personas con discapacidad intelectual y muestran inquietud por acercar al colectivo a foros de participación normalizados como lo son los certámenes de cortos y el acceso al arte en su globalidad</p>

