                <p class="text-justify" >  Las personas con discapacidad que forman parte de las asociaciones participan en esta convocatoria a través de <strong>cortos “caseros” o “amateur”</strong>,  mostrando al resto de entidades y a la comunidad en general mensajes positivos, en los términos que cada entidad considere, con la finalidad de romper el estigma que muy frecuentemente acompaña a este colectivo y con la firme creencia, que hoy más que nunca, nuestro grito debe escucharse.</p>

                <p class="text-justify" >El propósito de esta convocatoria es que las entidades presenten cortos, <strong>protagonizados por las personas con discapacidad</strong> y transmitiendo un enfoque positivo, divertido y optimista del día a día de las asociaciones.</p>

                <p class="text-justify" >Estos cortos estarán colgados en un espacio virtual con la finalidad de que los propios participantes y todos los interesados puedan valorarlos y puntuarlos como sus favoritos. No se persigue la competición, solo pasar un buen rato y acercar a las personas con discapacidad la realidad de otros centros similares al suyo a través del uso de las nuevas tecnologías.</p>

                <p class="text-justify" >Asimismo, y de forma opcional, las entidades participantes podrán realizar su cortometraje bajo la temática, LOS OCHENTA, TIEMPOS DE CAMBIO Y TRANSFORMACIÓN.</p>

