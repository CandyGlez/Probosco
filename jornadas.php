             <h3 style="color: #ff5757"> ¿QUIÉNES SOMOS? </h3>

             <p class="text-justify"> Asociación sin ánimo de lucro, que desde 1989, trabaja por la <strong> calidad de vida de las personas con discapacidad intelectual y sus familias</strong>. Desde 1994, Probosco es también un <strong>centro de  atención terapéutica de alto requerimiento</strong> que atiende a personas con discapacidad intelectual con grandes necesidades de apoyo. La misión de la entidad es <strong>facilitar proyectos de vida plena</strong> a las personas con discapacidad intelectual y sus familias. Con la visión de ser una organización fuerte, ilusionada, responsable, innovadora y unida, capaz de asesorar, apoyar /o responder a las necesidades individuales de las
                personas con discapacidad y sus familias.</p>

            <p class="text-justify"> Los valores que nos representan son la ilusión, superación y mejora continua; el compromiso y
                responsabilidad; el trabajo en equipo, respeto ycompañerismo; la ética; la comunicación; la seguridad; la transparencia y la unión. El compromiso de PROBOSCO es la <strong>mejora permanente de habilidades y herramientas </strong>para
                asegurarnos que nuestras acciones incidan directamente en la <strong>mejora de la calidad de vida</strong> de
                las personas para las que trabajamos.Nuestra labor diaria persigue conseguir la <strong>máxima participación social y ciudadana</strong>, asegurando así el <strong>ejercicio de los derechos</strong> de las personas con discapacidad intelectual. A su vez servimos como <strong>recurso formativo e informativo</strong> tanto para las familias, otros profesionales del ámbito de la discapacidad y para la comunidad en general.</p>
