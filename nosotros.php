                <h2>Quienes somos</h2>

                <p>En Probosco, queremos hacer realidad los proyectos de vida de las personas con discapacidad intelectual y sus familias, proporcionándoles para ello los apoyos y oportunidades necesarias desde un compromiso ético</p>
                <p>Somos un <strong>centro de atención a personas con discapacidad intelectual</strong> mayores de 18 años, con una media de edad por encima de los 35. Estas personas, muy diferentes entre sí en cuanto a sus preferencias, destrezas y  limitaciones, tienen en común el hecho de necesitar un <strong>alto grado de apoyo</strong> para desarrollar la vida diaria con normalidad. No obstante los niveles de autonomía difieren en cada caso.</p>
                <p>Establecemos nuestra atención en el marco del <strong>PARDIGMA DE APOYOS  de la AAMR</strong> y en el modelo de calidad de vida de Schalock y Verdugo. Uno de  los aspectos más destacados es la programación y el desarrollo  de actividades destinadas a aumentar la participación y el poder de decisión de las/os residentes.</p>
                <p>Actualmente el funcionamiento del centro se basa en  el modelo de <strong>planificación centrada en la persona </strong>que ya permite que las personas con discapacidad decidan sobre sus vidas. La gestión del centro está en función de los planes personales de los usuarios.</p>  

